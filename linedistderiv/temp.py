import numpy as np

A = np.array([
    [0, 0, 0, 0, 1],      # f(0, 0) = e
    [0, 1, 1, 1, 1],      # f(0, 1) = a + c + d + e
    [1, 0, 0, 0, 1],      # f(1, 0) = a + b + e
    [1, 1, 1, 1, 1],      # f(1, 1) = a + b + c + d + e
    [1, 0, 0, 0, 0],      # df/dx(0, 0) = a
    [1, 2, 0, 0, 0],      # df/dx(0, 1) = a + 2b
    [1, 0, 0, 0, 0],      # df/dx(1, 0) = a
    [1, 2, 0, 0, 0],      # df/dx(1, 1) = a + 2b
    [0, 0, 1, 2, 0],      # df/dy(0, 0) = c
    [0, 0, 1, 2, 0],      # df/dy(0, 1) = c + 2d
    [0, 0, 1, 0, 0],      # df/dy(1, 0) = c
    [0, 0, 1, 2, 0]       # df/dy(1, 1) = c + 2d
])

print(np.linalg.inv(A.T@A)@A.T)
